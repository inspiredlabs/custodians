	<h3 class="fw6 f6">Improving the UX of information retrieval</h3>
	<p>I am currently expanding the possibilities of <a href="https://www.instantwebapp.com/">my product idea</a>, by using LLMs. While LLMs highlight the opportunity to provide businesses better access to their information, most businesses lack understanding of how to deploy the right User Interface (UI).</p>

	<h3 class="fw6 f6">The problem using Language Models</h3>
	<p>Some businesses have already taken complicated steps to try and monetise their data through embedding, prompt-engineering and fine-tuning other companies propriertry LLMs. These approaches have value, but unless Human Computer Interaction (HCI) principles are applied the value-unlock will be negligible.</p>

	<h3 class="fw6 f6">The direction</h3>
	<p>I propose the following opportunity: help businesses solve HCI issues and create better information retrieval resources and develop ways to leverage LLM and transformer techologies.</p>
	<p>I have written more on the topic and I have several demo applications available to show my progress.</p>

